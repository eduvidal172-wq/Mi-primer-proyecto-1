public class Ejercicio10 {

    public static int mayor(int a, int b) {

        if (a > b) {
            return a;
        } else {
            return b;
        }
    }

    public static void main(String[] args) {

        int resultado = mayor(15, 8);

        System.out.println("El numero mayor es: " + resultado);
    }
}